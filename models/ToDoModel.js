const mongoose = require("mongoose");

// InventorySchema
const ToDoSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    status: {
      type: Boolean,
      default: true,
    },
  },
  { timestamps: true }
);

const ToDo = mongoose.model("ToDo", ToDoSchema);

module.exports = ToDo;
